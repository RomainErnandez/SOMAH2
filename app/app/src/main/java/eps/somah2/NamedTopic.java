public class NamedTopic {
    private int id;
    private String name;
    private int imageId;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return imageId;
    }
    public void setImage(int imageId) { this.imageId = imageId; }
}
